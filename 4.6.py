# 4.6 设置代码格式
# 编写代码时遵循格式设置约定可使代码易于阅读

# 4.6.1 格式设置指南
# Python社区提供一种关于Python语言增强和改进的提案，称为Python改进提案（PEP）
# PEP 8是最古老的PEP之一

# 4.6.2 缩进
# PEP 8建议每级缩进都使用4个空格
# 在程序中混合使用制表符和空格可能导致混乱

# 4.6.3 行长
# 很多Python程序员建议每行不超过80字符，但并非不可逾越
# PEP 8建议注释的行长不超过72字符，但并非不可逾越
# 长行建议适当拆分为多行

# 4.6.4 空行
# 将程序的不同部分分开可使用空行
# 但不要滥用

